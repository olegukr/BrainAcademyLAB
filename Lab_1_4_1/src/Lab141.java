// Write a console program prints on console each of the primitive data types
// (byte, short, int, long, float, double, char, boolean).

        byte B1 = 4;
        short S1 = 450;
        int I1 = 23_000;
        long L1 = 12_000_000;
        float F1 = 2.123E2F;
        double D1 = 5.11232;
